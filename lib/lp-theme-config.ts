export type NicheTheme = keyof typeof NICHE_THEMES

export function getNicheTheme(nicho: string): typeof NICHE_THEMES[NicheTheme] {
  const theme = NICHE_THEMES[nicho as NicheTheme]
  if (!theme) {
    console.warn(`Theme not found for nicho: ${nicho}, using default (automacao)`)
    return NICHE_THEMES.automacao
  }
  return theme
}

// Variações de estilo para criar aleatoriedade mantendo profissionalismo
export const STYLE_VARIATIONS = {
  rounded: {
    card: 'rounded-2xl',
    button: 'rounded-full',
    badge: 'rounded-full',
  },
  sharp: {
    card: 'rounded-lg',
    button: 'rounded-lg',
    badge: 'rounded-md',
  },
  mixed: {
    card: 'rounded-xl',
    button: 'rounded-lg',
    badge: 'rounded-lg',
  },
}

export const SHADOW_VARIATIONS = {
  soft: 'shadow-lg',
  medium: 'shadow-xl',
  strong: 'shadow-2xl',
}

export const ANIMATION_VARIATIONS = {
  subtle: 'transition-all duration-300',
  smooth: 'transition-all duration-500',
  bouncy: 'transition-all duration-300 transform hover:scale-105',
}

// Função para gerar variação baseada no slug (determinística mas parece aleatória)
export function getStyleVariation(slug: string) {
  const hash = slug.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0)
  
  const roundedStyles = Object.values(STYLE_VARIATIONS)
  const shadows = Object.values(SHADOW_VARIATIONS)
  const animations = Object.values(ANIMATION_VARIATIONS)
  
  return {
    rounded: roundedStyles[hash % roundedStyles.length],
    shadow: shadows[hash % shadows.length],
    animation: animations[hash % animations.length],
  }
}
